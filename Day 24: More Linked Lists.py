class Node:
    def __init__(self,data):
        self.data = data
        self.next = None 
class Solution: 
    def insert(self,head,data):
            p = Node(data)           
            if head==None:
                head=p
            elif head.next==None:
                head.next=p
            else:
                start=head
                while(start.next!=None):
                    start=start.next
                start.next=p
            return head  
    def display(self,head):
        current = head
        while current:
            print(current.data,end=' ')
            current = current.next

    def removeDuplicates(self,head):
        #Write your code here
        l = []
        if head is None:
            return 
        else:
            n = head
            while n is not None:
                l.append(n.data)
                n = n.next
        nums = sorted(list(set(l)))
        for i in nums:
            print(i, end=" ")
    
        
